
CREATE TABLE "ORA_LOG"."ERROR_LOG" (
	"ERROR_ID" NUMBER(12,0) NOT NULL,
	"ERROR_CODE" VARCHAR2(20 BYTE) NOT NULL,
	"ERROR_MESSAGE" VARCHAR2(400 BYTE),
	"CREATED_BY" VARCHAR2(30 BYTE),
	"CREATED_AT" DATE DEFAULT SYSDATE,
	CONSTRAINT "PK_ERROR_LOG" PRIMARY KEY ("ERROR_ID")
);

CREATE INDEX "ORA_LOG"."IDX_ERROR_LOG_CODE" ON "ORA_LOG"."ERROR_LOG" ("ERROR_CODE" ASC);

